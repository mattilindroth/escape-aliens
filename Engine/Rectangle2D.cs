namespace escape_aliens.Engine
{
    public class Rectangle2D 
    {
        public Rectangle2D(int x, int y, int w, int h)
        {
            X = x;
            Y = y;
            Width = w;
            Height = h;
        }
        public int X {get;set;}
        public int Y {get;set;}
        public int Height {get;set;}
        public int Width {get;set;}

        
    }
}